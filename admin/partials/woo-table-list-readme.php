<p>
    <strong>Woo Table List</strong> create a table view of your Woocommerce shop.</br>
    Full support of Woocommerce and Wordpress last versions, has a very low footprint that will not impact on your website performances.
</p>
<h3>Features</h3>

<ul class="wtl-list">
    <li>Easy settings</li>
    <li>Table view live rendering</li>
    <li>Works for Shop, Product Category, Related pages</li>
    <li>Switch from Woo Table List view to the current theme template without page reloading</li>
    <li>Access to Product variations thru a modal popup so users can order directly from the shop page with no page reloading</li>
    <li>Available Product fields/elements display filter : 
        <ul>
            <li>Image</li>
            <li>Product name</li>
            <li>Categories</li>
            <li>Rating</li>
            <li>Price</li>
            <li>Quantity</li>
            <li>Sale tag</li>
            <li>Add to cart button</li>
            <li>Custom Meta Field (will be displayed below product name)</li>
        </ul>
    </li>
    <li>Drag & Drop Fields order</li>
    <li>Full Ajax Add to cart support</li>
    <li>Plugin CSS used only to position elements. No override of theme style.</li>
    <li>Responsive output with same features available.</li>
</ul>


<p>
    <strong><i>Woo Table List doesn't change or override any current template of your theme. It's based on JavaScript and CSS functionalities in order to manipulate the output to render a table like view.</i></strong>
</p>
